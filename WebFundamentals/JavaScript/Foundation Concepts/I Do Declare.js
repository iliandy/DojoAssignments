function IdoDeclare(){
  var num1 = 0, num2 = -1, num3 = 3.14, num4 = 88;
  var str1 = "a", str2 = "b", str3 = "c";
  var bool1 = true, bool2 = false;
  var undef1 = undefined;

  console.log("Number 1 is:", num1);
  console.log("Number 2 is:", num2);
  console.log("Number 3 is:", num3);
  console.log("Number 4 is:", num4);

  console.log("String 1 is:", str1);
  console.log("String 2 is:", str2);
  console.log("String 3 is:", str3);

  console.log("Boolean 1 is:", bool1);
  console.log("Boolean 2 is:", bool2);

  console.log("Undefined 1 is:", undef1);
}
